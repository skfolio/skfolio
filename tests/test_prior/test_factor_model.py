def test_factor_model(X, y):
    model = FactorModel()
    model.fit(X, y)
    assert model.prior_model_
    assert model.prior_model_.mu.shape == (20,)
    np.testing.assert_almost_equal(
        model.prior_model_.cholesky @ model.prior_model_.cholesky.T,
        model.prior_model_.covariance,
        15,
    )

    model = FactorModel(
        residual_variance=False,
        loading_matrix_estimator=LoadingMatrixRegression(
            linear_regressor=LassoCV(cv=5, fit_intercept=False), n_jobs=-1
        ),
    )
    model.fit(X, y)
    assert model.prior_model_
    np.testing.assert_almost_equal(
        model.prior_model_.cholesky @ model.prior_model_.cholesky.T,
        model.prior_model_.covariance,
        15,
    )


