def test_dominate():
    assert dominate(np.array([1, 2, 3]), np.array([1, 2, 3])) == False
    assert dominate(np.array([1, 2, 3]), np.array([2, 3, 4])) == False
    assert dominate(np.array([2, 3, 4]), np.array([1, 2, 3])) == True
